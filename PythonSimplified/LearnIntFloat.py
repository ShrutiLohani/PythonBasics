# All about dealing with Numbers in python
# Firstly : Int = integer without decimals ; Float: with decimal


# to check the type of variable
x = 3
print(type(x))

# All operations

# Addition
print(3 + 2)

# Subtraction
print(3 - 2)

# Multiplication
print(3 * 2)

# Division
print(3 / 2)

# Floor Division : Removes the decimal part
print(3 // 2)

# Exponent
print(3 ** 2)

# Modulus
print(3 % 2)

# Absolute : removing negative sign
print(abs(-9))

# Rounding of the decimal
print(round(4.75))

# COMPARISON : Returns True/False
print(3 == 2)   # Equal
print(3 != 2)   # Not Equal
print(3 > 2)    # Greater
print(3 < 2)    # Smaller
print(3 >= 2)   # Greater or equal
